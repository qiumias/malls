<template>
  <div>
    <nav-bar>
      <template #left>
          <img src="@/assets/img/detail/back.svg" 
          class="img" @click="backClick">
      </template>
      <template #center>
          <div class="title">
              <div v-for="(item,index,key) in titles" 
              :key="key" class="title-item"
              :class="{active:index===currentIndex}"
              @click="titleClick(index)">{{item}}</div>
          </div>
      </template>
    </nav-bar>
  </div>
</template>

<script>
import NavBar from '@/components/common/navbar/NavBar'

export default {
    name:'DetailNavBar',
    components:{
        NavBar
    },
    data(){
        return{
            titles:['商品','参数','评论','推荐'],
            currentIndex:0
        }
    },
    methods:{
        titleClick(index){
            this.currentIndex=index
            this.$emit('titleClick',index)
        },
        backClick(){
            // this.$router.back
            this.$router.go(-1)
        }
    }
}
</script>

